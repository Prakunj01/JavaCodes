import java.util.*;
public class class_05{
    public static void main(String args[]){
        Scanner sc =new Scanner(System.in);
        
        int n = sc.nextInt();
        for(int i=0 ; i<n; i++){
            int j=0;
            while(j<n){
                System.out.print(" * ");
                j++;
            }
            System.out.print("\n");

        }
    }
}